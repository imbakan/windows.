bool FindExtension(const char* str)
{
	char* extensions = (char*)glGetString(GL_EXTENSIONS);
	return (strstr(extensions, str) != NULL);
}

void OutputExtenstions()
{
	char* str1 = (char*)glGetString(GL_EXTENSIONS);
	char str2[300];
	size_t i, j, n;

	n = strlen(str1);
	i = 0;

	while (i < n) {

		j = 0;

		while (str1[i] != ' ') {

			str2[j] = str1[i];

			++i;
			++j;
		}

		str2[j] = '\0';

		OutputDebugStringA(str2);
		OutputDebugStringA("\n");

		++i;
	}
}
